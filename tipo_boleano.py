"""
Tipo boleano, criado pela lógica boleana por George Boole

2 constantes, Verdadeiro e Falso

True and False (Sempre com a primeira letra maiúscula)

"""


ativo = True
print(ativo)

# Not - negação
print(not ativo)

# Ou - or:

print(ativo or False)

maior = 5 > 9
menor = 1 < 5
print(maior, menor)